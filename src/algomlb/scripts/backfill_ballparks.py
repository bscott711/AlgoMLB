def calculate_pm_coords(hp_lat, hp_lon, bearing_deg, dist_ft=60.5):
    """Calculate PM coordinates from HP + Bearing + Distance."""
    R = 6371000  # Earth radius in meters
    dist_m = dist_ft * 0.3048

    lat1 = radians(hp_lat)
    lon1 = radians(hp_lon)
    brng = radians(bearing_deg)

    lat2 = asin(sin(lat1) * cos(dist_m / R) + cos(lat1) * sin(dist_m / R) * cos(brng))
    lon2 = lon1 + atan2(
        sin(brng) * sin(dist_m / R) * cos(lat1), cos(dist_m / R) - sin(lat1) * sin(lat2)
    )

    return degrees(lat2), degrees(lon2)


